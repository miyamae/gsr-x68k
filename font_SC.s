TVRAM		equ	$e00000

	.xdef	print_sc

	.text
	.even

*********************************************************
*
*	ＳＣフォント文字列示ルーチン
*
*			入力	d6.w=Ｘ座標（８ドット単位）
*				d7.w=Ｙ座標（１ドット単位）
*				a6.l=表示文字列先頭
print_sc:
*********************************************************
	movem.l	d0-d3/d6-d7/a1-a2/a6,-(sp)

	movea.l	#TVRAM,a2		;座標決定
	lsl.l	#7,d7			;　d7=d7*(1024/8)
	adda.l	d7,a2			;　Ｙ
	adda.w	d6,a2			;　Ｘ

	clr.b	d3
	move.w	#16-1,d2
loop:
	moveq.l	#0,d1
	move.b	(a6)+,d1		;d1.b=ポインタ位置文字コード
	tst.b	d1			;終端コードなら以降をスペースにする
	bne	@f
	move.b	#1,d3
@@:
	tst.b	d3
	beq	@f
	move.b	#' ',d1
	bra	1f
@@:

	cmp.b	#' ',d1			;未対応コード？
	bge	@f
	move.b	#'~',d1
@@:	cmp.b	#'~',d1
	blt	@f
	move.b	#'~',d1
@@:
1:
	sub.b	#' ',d1
	lea.l	font_data,a1
**	lsl.l	#4,d1
	mulu.w	#14,d1
	adda.l	d1,a1

**	move.b	15(a1),1024/8*15(a2)	;テキスト書き込み
**	move.b	14(a1),1024/8*14(a2)	;	↓パターン下から上へ描いていく
	move.b	13(a1),1024/8*13(a2)
	move.b	12(a1),1024/8*12(a2)
	move.b	11(a1),1024/8*11(a2)
	move.b	10(a1),1024/8*10(a2)
	move.b	09(a1),1024/8*09(a2)
	move.b	08(a1),1024/8*08(a2)
	move.b	07(a1),1024/8*07(a2)
	move.b	06(a1),1024/8*06(a2)
	move.b	05(a1),1024/8*05(a2)
	move.b	04(a1),1024/8*04(a2)
	move.b	03(a1),1024/8*03(a2)
	move.b	02(a1),1024/8*02(a2)
	move.b	01(a1),1024/8*01(a2)
	move.b	00(a1),(a2)+

	dbra	d2,loop

break:
	movem.l	(sp)+,d0-d3/d6-d7/a1-a2/a6
	rts



